    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            String username = validateUsername(txtUsername.getText());
            String password = validatePassword(txtPassword.getText());

            Bibliotekar bibliotekar = new Bibliotekar();
            bibliotekar.setKorisnickoIme(username);
            bibliotekar.setSifra(password);

            Bibliotekar logged = ClientController.getInstance().loginBibliotekar(bibliotekar);
            JOptionPane.showMessageDialog(this, "Корисничко име и шифра су исправни.");

            try {
                dispose();
                new MainForm(logged).setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Не може да се отвори главна форма и мени.", "Грешка", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ClientValidationException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Валидација", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Неуспешна пријава", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed


    private String validateUsername(String value) throws ClientValidationException {
        if (value == null || value.isBlank()) {
            throw new ClientValidationException("Корисничко име је обавезно");
        }
        return value.trim();
    }

    private String validatePassword(String value) throws ClientValidationException {
        if (value == null || value.isBlank()) {
            throw new ClientValidationException("Лозинка је обавезна");
        }
        return value;
    }
